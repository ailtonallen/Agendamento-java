    public AgendamentoView() throws SQLException {
        initComponents();
        AgendamentoDAO agendamentoDAO = new AgendamentoDAO();
        List<Agendamento> agendamentos = agendamentoDAO.searchAgendamento();
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Nome do Cliente");
        model.addColumn("Serviço");
        model.addColumn("Data");
        model.addColumn("Preço");

        // Adicione os dados dos agendamentos ao modelo da tabela
        for (Agendamento agendamento : agendamentos) {
            Object[] rowData = {agendamento.getNomeCliente(), agendamento.getServico(), agendamento.getDate(), agendamento.getPrice()};
            model.addRow(rowData);
        }

        // Defina o modelo da tabela tblAgendamentos como o modelo personalizado
        tblAgendamentos.setModel(model);
    }

