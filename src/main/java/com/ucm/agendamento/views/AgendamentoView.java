    private void btnAgendarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgendarActionPerformed
        String nomeCliente = txtNomeCliente.getText();
        String servico = txtServico.getText();
        String price = txtPrice.getText();
        String date = dataAtual.getDayOfWeek().toString() + " " + dataAtual.getMonth().toString();
        Agendamento agendamento = new Agendamento(nomeCliente, servico, date, price);

        try {
            Connection conexao = new Conexao().getConnection();

            AgendamentoDAO agendamentoDAO = new AgendamentoDAO(conexao);
            agendamentoDAO.insert(agendamento);
            DefaultTableModel model = (DefaultTableModel) tblAgendamentos.getModel();
            model.addRow(new Object[]{nomeCliente, servico, date, price});
            txtNomeCliente.setText("");
            txtPrice.setText("");
            txtServico.setText("");

            JOptionPane.showMessageDialog(null, "Agendamento salvo com sucesso");

        } catch (SQLException ex) {
            Logger.getLogger(Agendamento.class.getName()).log(Level.SEVERE, null, ex);

        }// TODO add your handling code here:
    }//GEN-LAST:event_btnAgendarActionPerformed

